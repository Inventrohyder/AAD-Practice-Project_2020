package com.inventrohyder.aadpracticeproject2020;

public class Learner {
    private String name;
    private String country;
    private String badgeUrl;

    private int hours;

    private int score;

    public String getName() {
        return name;
    }

    public String getCountry() {
        return country;
    }

    public String getBadgeUrl() {
        return badgeUrl;
    }

    public int getHours() {
        return hours;
    }

    public int getScore() {
        return score;
    }
}
